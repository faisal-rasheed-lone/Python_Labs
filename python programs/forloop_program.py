# using simple for loop with range
print("simple for loop ")
for i in range(5):
    print("hello faisal")

# printing numbers using for loop
print("printing numbers ")
for i in range(1,10):
    print(i, end=" ")

# using for loop with increment more than 1
print("\n")
print(" for loop using increment in loop")
for i in range(1,10,2):
    print(i,end=" ")

# using for loop in reverse order
print('\n')
print(" for loop in reverse ")
for i in range(10,0,-1):
    print(i,end=" ")
